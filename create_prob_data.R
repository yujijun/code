test_matrix <- matrix(1:400, ncol=20)
a <- as.character(rep(1,20)) #character
b1 <- rep(0, 20)
b2 <- rep(0 ,20)
c1 <- rep(c(3,4),10)
c2 <- rep(c(3,4),10)
d1 <- c(rep(NA,5), rep(3,15))
d2 <- c(rep(NA,2),rep(4,18))
e <- rep(0,27)
test_matrix <- rbind(test_matrix, a,b1,b2,c1,c2,d1,d2)
test_matrix <- cbind(test_matrix, e, e)
rownames(test_matrix) <- paste0("G", 1:27)
colnames(test_matrix) <- paste0("S", 1:22)
write.table(test_matrix, "test_matrix.txt", sep = "\t")
